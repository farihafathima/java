package cgg.beans;

public class Student {

	
	
	//student properties
		private String name;
		private String rollNo;
	 
		//no-argument constructor
		public Student(){
	 
		}
	 
		public String getName() {
			return name;
		}
	 
		public void setName(String name) {
			this.name = name;
		}
	 
		public String getRollNo() {
			return rollNo;
		}
	 
		public void setRollNo(String rollNo) {
			this.rollNo = rollNo;
		}
}
